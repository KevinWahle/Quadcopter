/* Función que se llama 1 vez, al comienzo del programa */
void App_Init (void)
{
	gpioMode(PIN_LED_RED, OUTPUT);
	gpioMode(PIN_LED_GREEN, OUTPUT);
	gpioWrite(PIN_LED_RED, HIGH);
	gpioWrite(PIN_LED_GREEN, HIGH);

	PITInit(PIT_0, PIT_MS2TICK(1000), callback1);
	PITInit(PIT_1, PIT_MS2TICK(500), callback2);
	PITInit(PIT_2, PIT_MS2TICK(1000), callback3);

	PITStart(PIT_0);
	PITStart(PIT_1);
	// PIT_2 not started

}

